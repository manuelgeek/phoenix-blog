      assert account.avatar == "some avatar"
      assert account.bio == "some bio"
      assert account.fb == "some fb"
      assert account.tw == "some tw"
    end

    test "create_account/1 with invalid data returns error changeset" do
      assert {:error, %Ecto.Changeset{}} = Accounts.create_account(@invalid_attrs)
    end

    test "update_account/2 with valid data updates the account" do
      account = account_fixture()
      assert {:ok, %Account{} = account} = Accounts.update_account(account, @update_attrs)
      assert account.avatar == "some updated avatar"
      assert account.bio == "some updated bio"
      assert account.fb == "some updated fb"
      assert account.tw == "some updated tw"
    end

    test "update_account/2 with invalid data returns error changeset" do
      account = account_fixture()
      assert {:error, %Ecto.Changeset{}} = Accounts.update_account(account, @invalid_attrs)
      assert account == Accounts.get_account!(account.id)
